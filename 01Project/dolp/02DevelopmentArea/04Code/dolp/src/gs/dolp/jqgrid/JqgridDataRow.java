package gs.dolp.jqgrid;

import java.util.List;

public class JqgridDataRow {
	private int id;
	private List<String> cell;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public List<String> getCell() {
		return cell;
	}

	public void setCell(List<String> cell) {
		this.cell = cell;
	}

}
